        NewRoom nr = new NewRoom();
          int selection = 0;
        Room ro = new Room();
        ro.setIdRoom(1);
 //     ro.setCategory(category);
        ro.setCapacity(Integer.parseInt(this.jFTFCapacidad.getText()));
        ro.setPrice(Long.parseLong(this.jFTFPrecio.getText()));
