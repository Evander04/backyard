public boolean validaNull(){
    boolean val = false;
    if(this.jFTFCapacidad.getText().equals("")){
    val = true;
    } else if (this.jFTFPrecio.getText().equals("")){
    val = true;
    } else if (this.jTextADescripcion.getText().equals("")){
        val = true;
    }
    return val;
}
